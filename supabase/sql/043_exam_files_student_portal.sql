-- =============================================================================
-- 043 - Student exam file library
-- Dynamic data source for "Kho đề thi Tiếng Anh" in MV Klass.
-- Upload files to Supabase Storage (or any public/signed URL source), then add
-- rows here. The web app reads published rows without redeploying.
-- =============================================================================

create table if not exists public.exam_files (
  id uuid primary key default gen_random_uuid(),
  title text not null,
  level text not null check (level in ('entrance_10', 'university')),
  subject text not null default 'english' check (subject = 'english'),
  year integer,
  province text,
  category text not null default 'exam' check (category in ('exam', 'answer', 'audio', 'topic')),
  file_url text,
  answer_url text,
  audio_url text,
  description text,
  download_count integer not null default 0 check (download_count >= 0),
  created_at timestamptz not null default now(),
  is_published boolean not null default false
);

create index if not exists exam_files_published_idx
  on public.exam_files (is_published, subject, level, category, year desc, created_at desc);

comment on table public.exam_files is
  'Exam file library for student portal. Publish/unpublish rows without redeploying the web app.';

comment on column public.exam_files.file_url is
  'Main exam/topic file URL. Use a public Supabase Storage URL or a short-lived signed URL generated by admin tooling.';

