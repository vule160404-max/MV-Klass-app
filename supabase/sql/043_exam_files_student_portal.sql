insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values (
  'exam-files',
  'exam-files',
  true,
  52428800,
  array[
    'application/pdf',
    'image/png',
    'image/jpeg',
    'audio/mpeg',
    'audio/mp4',
    'audio/wav',
    'audio/ogg',
    'application/vnd.openxmlformats-officedocument.wordprocessingml.document'
  ]
)
on conflict (id) do update
set
  public = excluded.public,
  file_size_limit = excluded.file_size_limit,
  allowed_mime_types = excluded.allowed_mime_types;

alter table public.exam_files enable row level security;

drop policy if exists "exam_files_read_published" on public.exam_files;
create policy "exam_files_read_published"
on public.exam_files
for select
to authenticated
using (is_published = true);

drop policy if exists "exam_files_admin_teacher_all" on public.exam_files;
create policy "exam_files_admin_teacher_all"
on public.exam_files
for all
to authenticated
using (
  exists (
    select 1
    from public.profiles p
    where p.id = auth.uid()
      and p.role in ('admin', 'teacher')
  )
)
with check (
  exists (
    select 1
    from public.profiles p
    where p.id = auth.uid()
      and p.role in ('admin', 'teacher')
  )
);

create or replace function public.increment_exam_download_count(p_exam_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.exam_files
  set download_count = coalesce(download_count, 0) + 1
  where id = p_exam_id
    and is_published = true;
end;
$$;

grant select on public.exam_files to authenticated;
grant execute on function public.increment_exam_download_count(uuid) to authenticated;

